import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { pathToFileURL } from "node:url";

const root = resolve(import.meta.dirname, "..");
const outputPath = resolve(root, "docs/compatibility.md");
const modulePath = resolve(root, "packages/core/dist/exporters/capabilities.js");
const {
  CAPABILITY_MERMAID_VERSION,
  EXPORTER_CAPABILITIES,
  EXPORTER_FORMAT_SUMMARIES,
  MERMAID_DIAGRAM_CAPABILITIES,
} = await import(pathToFileURL(modulePath).href);

const formats = new Set(EXPORTER_CAPABILITIES.map(({ format }) => format));
const formatOrder = ["pptx", "svg", "drawio", "json-canvas"];
for (const expected of formatOrder) {
  if (!formats.has(expected)) throw new Error(`Missing ${expected} capability entries.`);
}

const summaryRows = EXPORTER_FORMAT_SUMMARIES.map(({ format, note, status }) =>
  `| ${cell(format)} | ${cell(status)} | ${cell(note)} |`);
const diagramTypes = [...new Set(MERMAID_DIAGRAM_CAPABILITIES.map(({ diagramType }) => diagramType))];
const diagramRows = diagramTypes.map((diagramType) => {
  const entries = MERMAID_DIAGRAM_CAPABILITIES.filter((entry) =>
    entry.diagramType === diagramType);
  if (entries.length !== formatOrder.length) {
    throw new Error(`${diagramType} must have one entry for every output format.`);
  }
  const statuses = formatOrder.map((format) =>
    entries.find((entry) => entry.format === format)?.status ?? "missing");
  return `| ${cell(diagramType)} | ${statuses.map(cell).join(" | ")} |`;
});
const featureRows = EXPORTER_CAPABILITIES.map(({ feature, format, mode, note, support }) =>
  `| ${cell(format)} | ${cell(mode ?? "—")} | ${cell(feature)} | ${cell(support)} | ${cell(note)} |`);
const content = [
  "# Export compatibility",
  "",
  "<!-- Generated by scripts/generate-compatibility.mjs. Do not edit manually. -->",
  "",
  `This matrix covers forward conversion from Mermaid ${CAPABILITY_MERMAID_VERSION} through the shared Diagram IR. Reverse import is out of scope.`,
  "",
  "## Current format summary",
  "",
  "| Format | Current level | Notes |",
  "| --- | --- | --- |",
  ...summaryRows,
  "",
  "Levels: **smart** uses native editable relationships with fallback; **editable** preserves target-native objects; **hybrid** keeps core semantics with diagnosed visual loss; **exact** is appearance-first; **planned** is not implemented; **unsupported** has no useful current mapping.",
  "",
  `## Mermaid ${CAPABILITY_MERMAID_VERSION} diagram types`,
  "",
  "| Diagram type | PPTX | SVG | draw.io | JSON Canvas |",
  "| --- | --- | --- | --- | --- |",
  ...diagramRows,
  "",
  "Non-flowchart PPTX entries marked **exact** require `mode: \"exact\"` and a source-SVG workflow to preserve the renderer appearance. DiagramIR-only exact mode embeds normalized IR SVG instead. Neither route claims editable internal nodes or edges.",
  "",
  "## Exporter feature details",
  "",
  "| Format | Mode | Feature | Support | Notes |",
  "| --- | --- | --- | --- | --- |",
  ...featureRows,
  "",
].join("\n");

if (process.argv.includes("--check")) {
  const current = await readFile(outputPath, "utf8").catch(() => "");
  if (current !== content) {
    console.error("docs/compatibility.md is stale. Run `pnpm compatibility:generate`.");
    process.exitCode = 1;
  } else {
    console.log("docs/compatibility.md matches the exporter capability registry.");
  }
} else {
  await writeFile(outputPath, content, "utf8");
  console.log("Generated docs/compatibility.md.");
}

function cell(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}
